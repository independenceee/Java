// Swap

public class Main {

    public static void main(String[] args) {

        String first = "Nguyễn Duy Khánh";
        String second = "Nguyễn Thị Giang";
        String temp;


        temp = first;
        first = second;
        second = temp;


        System.out.println("First: " + first);
        System.out.println("Second: " + second);

    }

}
